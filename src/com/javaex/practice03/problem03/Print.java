package com.javaex.practice03.problem03;

public class Print {
	void Printer(int p) {
		System.out.println(p);
	}
	
	void Printer(boolean p) {
		System.out.println(p);
	}
	
	void Printer(double p) {
		System.out.println(p);
	}
	
	void Printer(String p) {
		System.out.println(p);
	}
}
